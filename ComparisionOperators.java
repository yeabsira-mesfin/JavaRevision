/**
 * ComparisionOperators
 */
public class ComparisionOperators {

    public static void main(String[] args) {
        int x=1,y=1;
        System.out.println(x == y);
        System.out.println(x != y);
        System.out.println(x > y);
        System.out.println(x >= y);
        System.out.println(x < y);
        System.out.println(x <= y);
    }
}